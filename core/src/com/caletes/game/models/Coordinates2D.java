package com.caletes.game.models;

public class Coordinates2D {

    protected float x;

    protected float y;

    public Coordinates2D(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

}
